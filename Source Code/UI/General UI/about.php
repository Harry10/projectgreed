<p> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Project Greed is a web based stock market simulator created and developed by New Age Infrastructure (NAI). This website allows users to sign up, creating a user-name and password which gives users full access to the website, including the stock market simulator, a news feed, stock information and other functionality.
 <br><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Once becoming a member, users are able to play the stock market simulator by buying and selling stocks through a main portfolio, located in “Account/Portfolio”, which is accessible after signing up. In 
addition to the main simulation controlled by user's main portfolio, users can create virtual portfolio's which can be used to create and join competitions. Lastly, Project Greed offers 
a “What-if” simulator which allows members to create and view hypothetical stock transactions based on actual stock market information and the user's main portfolio. Using Yahoo Finance, Project Greed gives members a real life stock market experience with fun and easy to use simulations.
